from day5 import prog

from itertools import permutations

def compute_amplitude(input, perm):
    A = 0
    for p in perm:
        A = prog(input, [p, A], print_out=False)
    return A

def try_permutations(input):
    return max([compute_amplitude(input, perm) for perm in permutations(range(5))])


