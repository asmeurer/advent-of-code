def parse_input(text):
    all_moves = []
    for line in text.strip().splitlines():
        moves = []
        i = 0
        while i < len(line):
            c = line[i]
            if c in 'ns':
                moves.append(c + line[i+1])
                i += 2
                continue
            elif c in 'ew':
                moves.append(c)
                i += 1
            else:
                raise ValueError(f"Unexpected character {c}")
        all_moves.append(moves)
    return all_moves

