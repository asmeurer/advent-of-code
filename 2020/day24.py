def move_hex(x, y, move):
    if move == 'e':
        return (x + 1, y)
    elif move == 'w':
        return (x - 1, y)
    elif move == 'nw':
        if y % 2:
            return (x, y - 1)
        else:
            return (x - 1, y - 1)
    elif move == 'ne':
        if y % 2:
            return (x + 1, y - 1)
        else:
            return (x, y - 1)
    elif move == 'sw':
        if y % 2:
            return (x, y + 1)
        else:
            return (x - 1, y + 1)
    elif move == 'se':
        if y % 2:
            return (x + 1, y + 1)
        else:
            return (x, y + 1)
    else:
        raise ValueError(f"Unexpected move: {move!r}")

def make_moves(grid, moves):
    # 0 (the default) = white; 1 = black
    x, y = 0, 0
    for move in moves:
        x, y = move_hex(x, y, move)
    print(x, y, grid[x, y])
    grid[x, y] ^= 1
    return grid

def move_all(all_moves):
    grid = defaultdict(int)
    for moves in all_moves:
        grid = make_moves(grid, moves)
    return grid

