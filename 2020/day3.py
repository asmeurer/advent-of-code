import numpy as np

def make_array(text):
    return np.array([[0 if i == '.' else 1 for i in line] for line in
                     text.strip().splitlines()])

def count(a, right, down):
    cols, rows = a.shape
    tot = 0
    i, j = 0, 0
    while j < cols:
        print(i, j, a[j, i % rows])
        tot += a[j, i % rows]
        i += right
        j += down
    return tot

